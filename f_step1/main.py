import math

y_value = math.cos(math.radians(0))
print(f"cos(0°)={y_value}")
# cos(0°)=1.0

y_value = math.cos(math.radians(45))
print(f"cos(45°)={y_value}")
# cos(45°)=0.7071067811865476

y_value = math.cos(math.radians(89))
print(f"cos(89°)={y_value}")
# cos(89°)=0.0174524064372836

y_value = math.cos(math.radians(90))
print(f"cos(90°)={y_value}")
# cos(90°)=6.123233995736766e-17 ... ほぼ0

